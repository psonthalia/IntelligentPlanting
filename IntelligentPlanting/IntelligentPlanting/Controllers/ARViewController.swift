class ARViewController: UIViewController {

    @IBOutlet var sceneView: ARSCNView!
    
    @IBOutlet weak var brightnessLabel: UILabel!
    @IBOutlet weak var plantNameLabel: UILabel!
    
    var displayPlane = true

    var x:Float = 0.0
    var y:Float = 0.0
    var z:Float = 0.0
    var originalX:Float = 0.0
    
    var timer = Timer()
    
    static var plant: String?
    
    override func viewWillAppear(_ animated: Bool) {
        super.viewWillAppear(animated)
        setUpSceneView()
    }
    
    override func viewWillDisappear(_ animated: Bool) {
        super.viewWillDisappear(animated)
        sceneView.session.pause()
    }
    
    func setUpSceneView() {
        let configuration = ARWorldTrackingConfiguration()
        configuration.planeDetection = .horizontal
        
        configuration.isLightEstimationEnabled = true;  //enable lighting
        
        sceneView.session.run(configuration)
        
        sceneView.delegate = self
        sceneView.debugOptions = [ARSCNDebugOptions.showFeaturePoints]
    }
    
    func configureLighting() {
        sceneView.autoenablesDefaultLighting = true
        sceneView.automaticallyUpdatesLighting = true
    }
    
    func addTapGestureToSceneView() {
        let tapGestureRecognizer = UITapGestureRecognizer(target: self, action: #selector(ARViewController.setUpGrid(withGestureRecognizer:)))
        
        sceneView.addGestureRecognizer(tapGestureRecognizer)
    }
    
    @objc func setUpGrid(withGestureRecognizer recognizer: UIGestureRecognizer) {
        let tapLocation = recognizer.location(in: sceneView)
        let hitTestResults = sceneView.hitTest(tapLocation, types: .existingPlaneUsingExtent)
        
        guard let hitTestResult = hitTestResults.first else { return }
        let translation = hitTestResult.worldTransform.translation
        x = translation.x - 1.2
        originalX = translation.x - 1.2
        y = translation.y
        z = translation.z - 1.5
    }
    
    override func viewDidLoad() {
        super.viewDidLoad()
        
        // Set the view's delegate
        sceneView.delegate = self
        
        // Show statistics such as fps and timing information
        //sceneView.showsStatistics = true
        
        // Create a new scene
        //let scene = SCNScene(named: "art.scnassets/ship.scn")!
        
        // Set the scene to the view
        //sceneView.scene = scene
        
        addTapGestureToSceneView()
        configureLighting()
        
        timer = Timer.scheduledTimer(timeInterval: 1, target: self, selector: #selector(self.updateCounting), userInfo: nil, repeats: true)
        
        if let name = ARViewController.plant {
            plantNameLabel.text = name
        }
    }
    
    @objc func updateCounting() {
        if let estimate = self.sceneView.session.currentFrame?.lightEstimate {
            let brightness = estimate.ambientIntensity
            //let colorTemperature = estimate.ambientColorTemperature
            
            brightnessLabel.text = "Brightness: \(brightness)"
            
            //print("Light estimation: ")
            //print(estimate)
        } else {
            print("Light estimation not found")
        }
    }
    
